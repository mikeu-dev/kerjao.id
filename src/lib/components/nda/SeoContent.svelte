<div class="prose max-w-none prose-slate dark:prose-invert">
	<h2 class="text-3xl font-bold tracking-tight text-slate-900 dark:text-white">
		Pentingnya Perjanjian Kerahasiaan (NDA) dalam Bisnis
	</h2>
	<p>
		Dalam dunia bisnis yang kompetitif, ide, strategi, dan data teknis adalah aset yang sangat
		berharga. **Non-Disclosure Agreement (NDA)** atau Perjanjian Kerahasiaan adalah payung hukum
		yang memastikan bahwa informasi sensitif yang Anda bagikan kepada vendor, karyawan, atau calon
		mitra bisnis tidak akan bocor ke pihak luar atau kompetitor.
	</p>

	<div class="my-8 grid grid-cols-1 gap-6 md:grid-cols-2">
		<div class="rounded-2xl bg-indigo-50/50 p-6 dark:bg-slate-800/50">
			<h3 class="mt-0 font-bold text-indigo-900 dark:text-indigo-400">Keamanan Intektual</h3>
			<p class="mb-0 text-sm">
				Melindungi rahasia dagang, rumus, kode sumber, dan rencana strategis masa depan.
			</p>
		</div>
		<div class="rounded-2xl bg-emerald-50/50 p-6 dark:bg-slate-800/50">
			<h3 class="mt-0 font-bold text-emerald-900 dark:text-emerald-400">Kejelasan Hukum</h3>
			<p class="mb-0 text-sm">
				Menetapkan hak dan kewajiban masing-masing pihak jika terjadi kebocoran informasi.
			</p>
		</div>
	</div>

	<h3 class="text-xl font-bold text-slate-900 dark:text-white">Jenis NDA yang Umum Digunakan</h3>
	<ul>
		<li>
			<strong>NDA Satu Arah (Unilateral):</strong> Digunakan saat satu pihak (misal: Startup) mengungkapkan
			rahasia kepada pihak lain (misal: Vendor).
		</li>
		<li>
			<strong>NDA Dua Arah (Mutual):</strong> Digunakan saat kedua belah pihak saling berbagi rahasia,
			umum terjadi pada kolaborasi strategis atau merger.
		</li>
	</ul>

	<blockquote class="border-l-4 border-indigo-500 bg-slate-50 p-4 italic dark:bg-slate-800">
		"Kerahasiaan bukan sekadar masalah teknis, tapi fondasi dari kepercayaan profesional."
	</blockquote>

	<h3 class="text-xl font-bold text-slate-900 dark:text-white">Kapan Anda Butuh NDA?</h3>
	<p>Gunakan Generator NDA Kerjao saat Anda sedang:</p>
	<ol>
		<li>Mendiskusikan ide bisnis baru dengan investor atau mitra potensial.</li>
		<li>Memperkenalkan fitur baru kepada penguji (Beta Testers).</li>
		<li>Menyewa freelancer atau konsultan eksternal untuk proyek sensitif.</li>
		<li>Melibatkan outsource manufaktur atau logistik.</li>
	</ol>
