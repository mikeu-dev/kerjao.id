	<h3 class="text-xl font-bold text-slate-900 dark:text-white">Komponen Penting dalam Perjanjian Kerja</h3>
	<ul>
		<li><strong>Identitas Para Pihak:</strong> Nama perusahaan, alamat, dan data lengkap karyawan sesuai KTP.</li>
		<li><strong>Jabatan & Lokasi:</strong> Penjelasan spesifik mengenai posisi pekerjaan dan tempat penempatan tugas.</li>
		<li><strong>Upah & Tunjangan:</strong> Rincian gaji pokok dan tunjangan tetap yang harus dibayarkan perusahaan setiap periode.</li>
		<li><strong>Jangka Waktu:</strong> Tanggal mulai dan berakhirnya kontrak (khusus untuk PKWT).</li>
		<li><strong>Hak & Kewajiban:</strong> Penjelasan mengenai waktu kerja, cuti, serta tata tertib perusahaan.</li>
	</ul>

