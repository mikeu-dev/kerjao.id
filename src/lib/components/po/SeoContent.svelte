	<h2>Menerbitkan Purchase Order (PO) untuk Suplier</h2>
	<p>
		Purchase Order (PO) atau Surat Pemesanan Barang merupakan pelindung administratif bagi divisi
		*Procurement* perusahaan. Merilis lembaran PO ke suplai vendor menggaransi ketersediaan anggaran
		belanja dan membentengi bisnis dari risiko pengiriman barang yang tidak sesuai instruksi pesanan
		belanja pembukaan.
	</p>

	<h3>Anatomi PO Standar Industri</h3>
	<ul>
		<li>
			<strong>Nomor Referensi Sentral:</strong> Setiap dokumen wajib diagregasikan dengan identitas tiket
			unik (*PO Number*) guna melancarkan validasi internal Finance/Akunting serta penanda referensi korespondensi
			pada pihak eksternal (*Supplier*).
		</li>
		<li>
			<strong>Rincian Kuantitas dan Pajak:</strong> Cantumkan harga satuan (*Unit Price*) yang dikalikan
			konversi jumlah. Lalu lekatkan kebijakan beban PPn (Pajak Pertambahan Nilai) jika mitra vendor adalah
			PKP (Pengusaha Kena Pajak). Dapur kerjao mem-*by-pass* kalkulus berbelit ini secara otomatis.
		</li>
	</ul>

	<p>
		Tingkatkan gengsi perusahaan B2B Anda lewat pencetakan PO kilat dari Kerjao. Desain tabel
		*spreadsheet* dirender langsung di dalam halaman web dan ditransformasikan jadi wujud *invoice*
		order *print-out* otoritatif siap kirim.
	</p>
