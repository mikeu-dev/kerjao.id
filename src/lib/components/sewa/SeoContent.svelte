	<h2>Pentingnya Surat Perjanjian Sewa Menyewa Tertulis</h2>
	<p>
		Menyewakan properti tanpa dokumen tertulis adalah risiko besar bagi pemilik maupun penyewa. 
		Surat Perjanjian Sewa Menyewa (SPSM) berfungsi sebagai alat bukti hukum jika terjadi sengketa atau kerusakan properti di kemudian hari.
	</p>

	<h3>Elemen Penting dalam Perjanjian Sewa</h3>
	<ul>
		<li><strong>Identitas NIK KTP:</strong> Memastikan pihak-pihak yang terlibat adalah identitas asli untuk kebutuhan pelaporan jika terjadi masalah.</li>
		<li><strong>Deskripsi Objek:</strong> Mencatat kondisi fisik awal dan fasilitas (seperti AC atau furnitur) agar tidak disalahgunakan.</li>
		<li><strong>Uang Jaminan (Deposit):</strong> Sebagai dana cadangan jika penyewa meninggalkan tunggakan tagihan listrik atau kerusakan struktur.</li>
	</ul>

	<h3>Tips Bagi Pemilik Properti</h3>
	<p>
		Selalu lakukan inspeksi bersama sebelum menyerahkan kunci. Lampirkan foto kondisi properti sebagai lampiran tambahan dalam draf fisik Anda. 
		Gunakan format <em>Justified</em> dalam pencetakan agar dokumen terlihat formal dan profesional di mata hukum.
	</p>
