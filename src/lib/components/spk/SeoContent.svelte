	<h2>Apa itu Surat Perintah Kerja (SPK) dan Perannya dalam Bisnis?</h2>
	<p>
		Surat Perintah Kerja (SPK) adalah dokumen instruksi resmi dari pemilik proyek kepada pihak pelaksana (vendor atau individu). 
		Fungsi utamanya adalah sebagai dasar hukum untuk memulai pekerjaan sebelum atau sesudah kontrak ditandatangani.
	</p>

	<h3>Mengapa SPK Sangat Krusial?</h3>
	<ul>
		<li><strong>Kepastian Administrasi:</strong> Menghilangkan keraguan verbal mengenai kapan pekerjaan harus dimulai dan apa saja cakupannya.</li>
		<li><strong>Bukti Penagihan:</strong> SPK sering menjadi syarat mutlak (lampiran) saat mengajukan invoice atau termin pembayaran.</li>
		<li><strong>Mitigasi Risiko:</strong> Menetapkan batas waktu (deadline) yang jelas untuk menghindari keterlambatan proyek.</li>
	</ul>

	<h3>Perbedaan SPK dengan Purchase Order (PO)</h3>
	<p>
		Meskipun serupa, Purchase Order (PO) biasanya digunakan untuk pembelian barang atau jasa standar, sedangkan SPK lebih sering digunakan untuk 
		pekerjaan yang memiliki durasi, lokasi fisik, atau ruang lingkup teknis yang kompleks (seperti renovasi, pembuatan software, atau jasa konsultasi).
	</p>
