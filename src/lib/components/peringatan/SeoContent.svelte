	<h2>Menerbitkan Surat Peringatan Karyawan (SP 1, 2, 3)</h2>
	<p>
		Surat Peringatan (SP) adalah mekanisme indispliner bertahap yang membina dan mendisiplinkan
		performa karyawan dalam korporasi di Indonesia. Kesalahan prosedur pada pelayangan SP bisa
		berujung pada gugatan pelanggaran undang-undang ketenagakerjaan.
	</p>

	<h3>Prosedur Sah HRD</h3>
	<ul>
		<li>
			<strong>Deskripsi Kasus Objektif:</strong> SP yang sah dilarang memuat frasa ambigu. Cantumkan langsung
			klausa deskripsi perihal pelanggaran dan momen tanggal indispliner tersebut terjadi secara riil.
		</li>
		<li>
			<strong>Otorisasi Pemangku Kepentingan:</strong> Surat Peringatan hanya mengikat secara valid bilamana
			mendapat persetujuan pendamping dari Manajer Personalia atau Kepala Unit HRD langsung.
		</li>
	</ul>

	<p>
		Draf form Surat Peringatan kami memfasilitasi format penyusunan instan dari tahapan SP 1, 2,
		hingga tingkatan mutlak ke-3 (Putus Hubungan Kerja) dengan sangat rapi. Menelurkan sanksi
		peringatan korporat presisi kini bebas kerumitan birokrasi.
	</p>
