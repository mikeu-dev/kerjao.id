	<h2>Panduan Sah Membuat Surat Kuasa</h2>
	<p>
		Dalam birokrasi, Surat Kuasa adalah instrumen sah untuk memindahkan wewenang, baik untuk
		mengurus dokumen seperti BPKB, STNK, Paspor, atau administrasi bank seperti pencairan deposito,
		jika pemberi kuasa terhalang untuk hadir secara fisik.
	</p>

	<h3>Ketentuan Keabsahan</h3>
	<ul>
		<li>
			<strong>Informasi Pihak:</strong> Identitas otentik KTP (Pemberi dan Penerima Kuasa) adalah hal
			terpenting yang wajib dilampirkan berdasarkan parameter dukcapil.
		</li>
		<li>
			<strong>Batasan Kuasa:</strong> Hindari penyusunan diksi bermakna kuasa absolut. Pastikan Anda hanya
			menulis spesifik untuk keperluan tertentu saja agar mencegah penyelewengan kuasa.
		</li>
		<li>
			<strong>Pembubuhan Materai:</strong> Untuk jaminan payung hukum materiel, pastikan kertas Surat
			Kuasa ditandatangani di atas selembar materai legal terbaru khusus di sisi penandatangan "Pemberi
			Kuasa". Kami mendesain kotak meterai fisik pada elemen PDF ini.
		</li>
	</ul>

