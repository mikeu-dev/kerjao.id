	<h2>Tips Sukses Membuat Surat Lamaran Kerja (Cover Letter)</h2>
	<p>
		Surat Lamaran Kerja adalah representasi pertama yang dilihat oleh tim HR sebelum membaca CV
		Anda. Di era rekrutmen digital, kelengkapan dan format yang tepat menjadi kunci untuk memastikan
		lamaran Anda dipertimbangkan.
	</p>

	<h3>Poin Penting dalam Penulisan</h3>
	<ul>
		<li>
			<strong>Bahasa yang Baku dan Sopan:</strong> Pastikan seluruh struktur menggunakan Ejaan Bahasa
			Indonesia (EBI) yang baik. Hindari menggunakan kata ganti informal.
		</li>
		<li>
			<strong>Fokus Pada Posisi:</strong> Sebutkan secara eksplisit posisi pekerjaan yang Anda tuju
			(misalnya <em>Front-End Developer</em> atau <em>Admin Keuangan</em>).
		</li>
		<li>
			<strong>Singkat, Padat, Jelas:</strong> Surat lamaran yang ideal tidak melebihi 1 halaman kertas
			A4. Jangan mengulang isi CV secara berlebihan.
		</li>
	</ul>

	<h3>Mengapa Menggunakan Generator Kami?</h3>
	<p>
		Alat ini menyusun <em>Cover Letter</em> yang terstruktur sesuai dengan kaidah rekrutmen di
		Indonesia. Teks yang tergenerasi diformat sedemikian rupa sehingga memberi kesan terdidik bagi
		pembacanya. Pemrosesan terjadi secara lokal melalui peramban <em>(client-side)</em>, memastikan
		data diri privasi Anda tidak akan pergi kemanapun.
	</p>
