	<h2>Panduan dan Tips Surat Resign (Pengunduran Diri)</h2>
	<p>
		Mengajukan pengunduran diri secara profesional adalah langkah penting untuk menjaga hubungan
		baik dengan perusahaan dan rekan kerja. Menggunakan generator surat resign online kami, Anda
		dapat membuat draf surat pengunduran diri dalam hitungan detik.
	</p>

	<h3>Apa yang Perlu Disiapkan Sebelum Resign?</h3>
	<ul>
		<li>
			<strong>Pemberitahuan (Notice Period):</strong> Periksa kembali kontrak kerja Anda. Umumnya,
			perusahaan mensyaratkan pemberitahuan 1 bulan sebelumnya (<em>one month notice</em>).
		</li>
		<li>
			<strong>Bicarakan dengan Atasan:</strong> Sebelum menyerahkan surat resmi, selalu sempatkan untuk
			berdiskusi lisan terlebih dahulu dengan atasan langsung.
		</li>
		<li>
			<strong>Rencana Transisi (Handover):</strong> Siapkan dokumen atau catatan untuk serah terima pekerjaan
			agar operasional tim tetap berjalan lancar setelah Anda pergi.
		</li>
	</ul>

	<h3>Mengapa Menggunakan Generator Surat Resign kerjao.id?</h3>
	<p>
		Generator kami dirancang khusus untuk memenuhi standar profesionalisme di Indonesia dengan
		beberapa pilihan gaya bahasa:
	</p>
	<ul>
		<li><strong>Formal:</strong> Cocok untuk perusahaan korporat atau institusi pemerintahan.</li>
		<li><strong>Profesional:</strong> Standard industri modern, sopan namun tidak terlalu kaku.</li>
		<li><strong>Santai:</strong> Cocok untuk startup atau agensi kreatif dengan budaya rileks.</li>
	</ul>

